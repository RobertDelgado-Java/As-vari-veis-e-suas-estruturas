package variáveis_e_sua_estrutura;

public class EstruturaDasVariaveis {
	
	/*Praticando as diferentes estruturas das variáveis*/
	
	public static void main(String[] args) {
		
		int idade = 28;
		String nome = "Oner";
		String sobrenome = "Merkei";
		String cpf = "156.896.745-65";
		
		System.out.println("O meu nome é :" + nome);
		System.out.println("O meu sobrenome é: " + sobrenome);
		System.out.println("A minha idade é: " + idade);
		System.out.println("O meu CPF é: " + cpf);
		
	}
	
	
}
